package org.com.model;

public class RoadPointBean {
	private String name;
	private Integer indexInRoad;
	private Double x;
	private Double y;
	private Double z;

	public RoadPointBean() {

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getIndexInRoad() {
		return indexInRoad;
	}

	public void setIndexInRoad(Integer indexInRoad) {
		this.indexInRoad = indexInRoad;
	}

	public Double getX() {
		return x;
	}

	public void setX(Double x) {
		this.x = x;
	}

	public Double getY() {
		return y;
	}

	public void setY(Double y) {
		this.y = y;
	}

	public Double getZ() {
		return z;
	}

	public void setZ(Double z) {
		this.z = z;
	}
}
